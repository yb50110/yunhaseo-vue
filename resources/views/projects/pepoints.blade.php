    <h1 class="title">PE Points Web Application</h1>
    <p class="subtitle">2016; B302: A Creative Media Mob</p>

    <p class="body">
        HAN University of Arnhem, Netherlands incorporates a point system that all students must fulfill before they graduate.
        Previously, the history of points were maintained in an outdated and not user-friendly system, which made it
        extremely difficult for the students as well as the school staff.
        <br><br>
        Our team, which consisted of a Graphic Designer, Front-end Developer, Back-end Developer, and a Project Manager,
        proposed a new design and built the application. There were numerous feedback sessions with the clients where
        we showcased our progress, reviewed new functions and features, and clarified assumptions and project goals.
        <br><br>
        As being one of the developers' and designer's last major projects at B302, it went extremely smoothly - even with
        the initial hiccup of the project that occurred with the transition of a new Project Manager. Active communication
        took place between the designer and myself, as the Front-end Developer, to discuss what design choices were
        feasible with the schedule.
    </p>


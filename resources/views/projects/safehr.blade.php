    <h1 class="title">SAFEHR Portal and App</h1>
    <p class="subtitle">2017 - Present; B507: A Creative Media Mob</p>

    <p class="body">
        Perhaps the largest and most complex project I have been a part of, SAFEHR is a three-year old and currently
        running project that includes an EHR and a content management system meant for use in the simulation labs
        of the Minnesota State University, Mankato School of Nursing (Glenn taylor institute).
        <br><br>

        The SAFEHR App is meant for classroom assignment and simulation use. It includes an Electronic Health Record
        (EHR) system with a focus on the family aspect of the patient, including the Family Constructs, Genogram
        and Ecomap systems that tracks the families and emotional connections of the patient.
        <br><br>
    </p>

    <div class="image-full ipad" style="background-image: url('./img/safehr/safehr-app-home.png');"></div>
    <div class="image-full ipad" style="background-image: url('./img/safehr/safehr-app-ehr.png');"></div>

    <p class="body">
        The SAFEHR Portal is for the Nursing Faculty to easily enter information of the scenarios for the simulations.
        They can also view more information relating to their courses and students, and assign specific patients
        to students to use in the classroom as an assignment.
        <br><br>
    </p>

    <div class="image-full ipad" style="background-image: url('./img/safehr/safehr-portal-scenarios.png');"></div>

    <p class="body">
        The project currently has 6 developers and 1 project manager, where none of members have been around since the
        dawn of the project. Thus, there have been countless communication problems, code conflicts, scope creeps,
        and disconnect from the vision of the client. Bettering communication and writing legible code has been a
        continuous effort by every team member.
        <br><br>

        Without a doubt, there have been many internal conflict between the members. I had a personal conflict with
        one of the members but recognized that it should not affect the professional work environment. I spoke with my
        project manager, who is also our office manager, and he gave me some advice and steps I could take to resolve
        it. Following his suggestions, I had a conversation with that member, expressing my frustrations and
        apologizing for my hostility. Since then, my relationship with this member has improved tremendously,
        which has boosted the morals and cooperation needed to complete assigned tasks.
    </p>

